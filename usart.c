/**
 * @brief CRC16_MODBUS 计算
 * @param buf: 数据缓冲区
 * @param len: 数据长度
 * @retval CRC16 值
 */
uint16_t CRC16_MODBUS(uint8_t *buf, uint16_t len)
{
    uint16_t crc = 0xFFFF;
    for (uint16_t i = 0; i < len; i++)
    {
        crc ^= buf[i];
        for (uint8_t j = 0; j < 8; j++)
        {
            if (crc & 0x0001)
            {
                crc = (crc >> 1) ^ 0xA001;
            }
            else
            {
                crc >>= 1;
            }
        }
    }
    return crc;
}

/**
 * @brief RS485 发送数据
 * @param txBuf: 发送缓冲区
 * @param len: 数据长度
 */
void RS485_Send(uint8_t *txBuf, uint16_t len)
{
    RS485_TX_MODE();
    HAL_Delay(1);
    HAL_UART_Transmit(&huart2, txBuf, len, 1000);
    while (__HAL_UART_GET_FLAG(&huart2, UART_FLAG_TC) == RESET);
    HAL_Delay(1);
    RS485_RX_MODE();
}

/**
 * @brief RS485 接收数据
 * @param rxBuf: 接收缓冲区
 * @param maxLen: 最大接收长度
 * @param timeout: 超时时间(ms)
 * @retval 接收到的数据长度，-1表示超时
 */
int16_t RS485_Receive(uint8_t *rxBuf, uint16_t maxLen, uint32_t timeout)
{
    uint16_t rxSize = 0;
    if (HAL_UARTEx_ReceiveToIdle(&huart2, rxBuf, maxLen, &rxSize, timeout) == HAL_OK)
    {
        return rxSize;
    }
    return -1;
}

